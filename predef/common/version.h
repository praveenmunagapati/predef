#ifndef __PREDEF_VERSION_H
#define __PREDEF_VERSION_H

#define PREDEF_VERSION_MAKE(major,minor,patch)  ((((major)%100)*10000000)+(((minor)%100)*100000)+((patch)%100000))
#define PREDEF_VERSION_MAKE_DATE(Y,M,D)         PREDEF_VERSION_MAKE((Y)%10000-1970,(M)%100,(D)%100)
#define PREDEF_VERSION_MAKE_YYYYMMDD(V)         PREDEF_VERSION_MAKE_DATE(((V)/10000)%10000,((V)/100)%100,(V)%100)
#define PREDEF_VERSION_MAKE_YYYY(V)             PREDEF_VERSION_MAKE_DATE(V,1,1)
#define PREDEF_VERSION_MAKE_YYYYMM(V)           PREDEF_VERSION_MAKE_DATE((V)/100,(V)%100,1)
#define PREDEF_VERSION_MAKE_0X_VRP(V)           PREDEF_VERSION_MAKE((V&0xF00)>>8,(V&0xF0)>>4,(V&0xF))
#define PREDEF_VERSION_MAKE_0X_VVRP(V)          PREDEF_VERSION_MAKE((V&0xFF00)>>8,(V&0xF0)>>4,(V&0xF))
#define PREDEF_VERSION_MAKE_0X_VRPP(V)          PREDEF_VERSION_MAKE((V&0xF000)>>12,(V&0xF00)>>8,(V&0xFF))
#define PREDEF_VERSION_MAKE_0X_VVRR(V)          PREDEF_VERSION_MAKE((V&0xFF00)>>8,(V&0xFF),0)
#define PREDEF_VERSION_MAKE_0X_VRRPPPP(V)       PREDEF_VERSION_MAKE((V&0xF000000)>>24,(V&0xFF0000)>>16,(V&0xFFFF))
#define PREDEF_VERSION_MAKE_0X_VVRRP(V)         PREDEF_VERSION_MAKE((V&0xFF000)>>12,(V&0xFF0)>>4,(V&0xF))
#define PREDEF_VERSION_MAKE_0X_VRRPP000(V)      PREDEF_VERSION_MAKE((V&0xF0000000)>>28,(V&0xFF00000)>>20,(V&0xFF000)>>12)
#define PREDEF_VERSION_MAKE_0X_VVRRPP(V)        PREDEF_VERSION_MAKE((V&0xFF0000)>>16,(V&0xFF00)>>8,(V&0xFF))
#define PREDEF_VERSION_MAKE_10_VPPP(V)          PREDEF_VERSION_MAKE(((V)/1000)%10,0,(V)%1000)
#define PREDEF_VERSION_MAKE_10_VRP(V)           PREDEF_VERSION_MAKE(((V)/100)%10,((V)/10)%10,(V)%10)
#define PREDEF_VERSION_MAKE_10_VRP000(V)        PREDEF_VERSION_MAKE(((V)/100000)%10,((V)/10000)%10,((V)/1000)%10)
#define PREDEF_VERSION_MAKE_10_VRPP(V)          PREDEF_VERSION_MAKE(((V)/1000)%10,((V)/100)%10,(V)%100)
#define PREDEF_VERSION_MAKE_10_VRR(V)           PREDEF_VERSION_MAKE(((V)/100)%10,(V)%100,0)
#define PREDEF_VERSION_MAKE_10_VRRPP(V)         PREDEF_VERSION_MAKE(((V)/10000)%10,((V)/100)%100,(V)%100)
#define PREDEF_VERSION_MAKE_10_VRR000(V)        PREDEF_VERSION_MAKE(((V)/100000)%10,((V)/1000)%100,0)
#define PREDEF_VERSION_MAKE_10_VV00(V)          PREDEF_VERSION_MAKE(((V)/100)%100,0,0)
#define PREDEF_VERSION_MAKE_10_VVRR(V)          PREDEF_VERSION_MAKE(((V)/100)%100,(V)%100,0)
#define PREDEF_VERSION_MAKE_10_VVRRPP(V)        PREDEF_VERSION_MAKE(((V)/10000)%100,((V)/100)%100,(V)%100)
#define PREDEF_VERSION_MAKE_10_VVRR0PP00(V)     PREDEF_VERSION_MAKE(((V)/10000000)%100,((V)/100000)%100,((V)/100)%100)
#define PREDEF_VERSION_MAKE_10_VVRR0PPPP(V)     PREDEF_VERSION_MAKE(((V)/10000000)%100,((V)/100000)%100,(V)%10000)
#define PREDEF_VERSION_MAKE_10_VVRR00PP00(V)    PREDEF_VERSION_MAKE(((V)/100000000)%100,((V)/1000000)%100,((V)/100)%100)

#define PREDEF_VERSION_MAX                      PREDEF_VERSION_MAKE(99,99,99999)
#define PREDEF_VERSION_ZERO                     PREDEF_VERSION_MAKE(0,0,0)
#define PREDEF_VERSION_MIN                      PREDEF_VERSION_MAKE(0,0,1)

#define PREDEF_VERSION_AVAILABLE                PREDEF_VERSION_MIN
#define PREDEF_VERSION_NOT_AVAILABLE            PREDEF_VERSION_ZERO

#endif